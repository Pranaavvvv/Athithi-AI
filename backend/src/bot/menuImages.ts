// Function to get menu image URL based on package
export function getMenuImageUrl(packageTier: string): string {
  return MENU_IMAGES[packageTier as keyof typeof MENU_IMAGES]?.url || MENU_IMAGES.standard.url;
}

// Function to get menu structure based on package
export function getMenuStructure(packageTier: string) {
  return MENU_STRUCTURE[packageTier as keyof typeof MENU_STRUCTURE] || MENU_STRUCTURE.standard;
}

// Function to parse user selection from menu numbers
export function parseMenuSelection(packageTier: string, selection: string): { category: string, items: string[] }[] {
  const structure = getMenuStructure(packageTier);
  const result: { category: string, items: string[] }[] = [];
  
  // Parse selections like "Starters: 1,2,3; Mains: 1,3,5; Desserts: 1,2"
  const categories = selection.split(';').map(cat => cat.trim());
  
  for (const categorySelection of categories) {
    const [categoryPart, itemsPart] = categorySelection.split(':').map(part => part.trim());
    
    if (!itemsPart) continue;
    
    let categoryKey = '';
    if (categoryPart.toLowerCase().includes('starter')) categoryKey = 'starters';
    else if (categoryPart.toLowerCase().includes('main')) categoryKey = 'mains';
    else if (categoryPart.toLowerCase().includes('dessert')) categoryKey = 'desserts';
    else if (categoryPart.toLowerCase().includes('beverage')) categoryKey = 'beverages';
    
    if (categoryKey && structure[categoryKey as keyof typeof structure]) {
      const numbers = itemsPart.split(',').map(num => parseInt(num.trim())).filter(n => !isNaN(n));
      const items = numbers.map(num => {
        const item = (structure[categoryKey as keyof typeof structure] as any[]).find((i: any) => i.number === num);
        return item ? item.name : null;
      }).filter(Boolean);
      
      if (items.length > 0) {
        result.push({ category: categoryKey, items });
      }
    }
  }
  
  return result;
}
